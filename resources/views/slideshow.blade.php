    <script type="module">
        import Swiper from 'https://unpkg.com/swiper/swiper-bundle.esm.browser.min.js'
        (() => {
            let galleryThumbs = new Swiper('.gallery-thumbs', {
                spaceBetween: 10,
                slidesPerView: 4,
                freeMode: true,
                watchSlidesVisibility: true,
                watchSlidesProgress: true,
            });
            let galleryTop = new Swiper('.gallery-top', {
                spaceBetween: 10,
                navigation: {
                    nextEl: '.swiper-button-next',
                    prevEl: '.swiper-button-prev',
                },
                thumbs: {
                    swiper: galleryThumbs
                }
            });

            let buildSlide = (slide) => `
                <div class="swiper-slide" style="background-image:url(${slide.image || slide.previewUrl});">
                    <a href="${slide.url}"><div class="title">${slide.name}</div></a>
                    <div class="subtitle">${slide.name}</div>
                    <div class="text">
                        <p>${slide.desc}</p>
                    </div>
                </div>`;

            let buildThumb = (slide) => `<div class="swiper-slide" style="background-image:url(${slide.image || slide.previewUrl})"></div>`;

            fetch('http://localhost/api/slides/6')
                .then(response => response.json())
                .then(data => {
                    console.log(data);
                    if (!data || data.length <= 0) {
                        alert('The specified campaign does not have any media to display.');
                        return;
                    }
                    let slides = [];
                    let thumbs = [];
                    let noImages = [];
                    data.forEach((slide) => {
                        if (!slide.image) {
                            //Put any slides that dont have pretty images at the end of the slideshow.
                            noImages.push(slide);
                        } else {
                            slides.push(buildSlide(slide));
                            thumbs.push(buildThumb(slide));
                        }
                    });
                    noImages.forEach((slide) => {
                        slides.push(buildSlide(slide));
                        thumbs.push(buildThumb(slide));
                    });
                    galleryTop.appendSlide(slides);
                    galleryThumbs.appendSlide(thumbs);
                });
        })();
    </script>
